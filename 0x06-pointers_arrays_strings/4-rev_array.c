/**
 *reverse_array - A function that reverses the content of an array of integers.
 *@n: The number of elements of the array
 *@a: Array to be reversed.
 */

void reverse_array(int *a, int n)
{
	int i, j, k;

	j = n - 1;

	for (i = 0; i < n / 2; i++)
	{
		k = a[i];
		a[i] = a[j];
		a[j--] = k;
	}
}
